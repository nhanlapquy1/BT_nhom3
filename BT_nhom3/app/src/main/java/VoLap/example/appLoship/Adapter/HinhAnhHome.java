package VoLap.example.appLoship.Adapter;

public class HinhAnhHome {
    private String Ten;
    private  int Hinh;

    public HinhAnhHome(String ten, int hinh) {
        Ten = ten;
        Hinh = hinh;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        Ten = ten;
    }

    public int getHinh() {
        return Hinh;
    }

    public void setHinh(int hinh) {
        Hinh = hinh;
    }
}


